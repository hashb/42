int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	if (s1[0] > s2[0])
		return (s1[0] - s2[0]);
	else if (s1[0] < s2[0])
		return (s1[0] - s2[0]);
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] == s2[i])
			i++;
		else
			return (s1[i] - s2[i]);
	}
	return (0);
}
